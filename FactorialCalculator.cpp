#include <iostream>
using namespace std;

int main (){
  int x, y, w = 0, z = 0;
   cout << "Multiplicacion de los dos numeros entre dos numeros" << endl;
   cout << "Introduce el primer numero\n";
   cin >> x;
   cout << "Introduce el segundo numero\n";
   cin >> y;

  while (x <= y) {
    int w=0;
    (w = x+1);
    (z = (w)*(x+1)); }
  cout << "Total= " << (z*w) << "\n";
  return 0;

}
